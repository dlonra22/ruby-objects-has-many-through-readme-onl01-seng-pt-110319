class Customer
  class Customer
  attr_accessor :name, :age
 
  @@all = []
 
  def initialize(name, age)
    @name = name
    @age = age
    @@all << self
  end
 
  def self.all
    @@all
  end
  
  def new_meal(waiter, total, tip=0)
    Meal.new(waiter, self, total, tip)
  end
  
  def waiters
   meals = Meal.all.select {|m| m.customer == self
   meals.collect { |ml| ml.waiter}
  end
end